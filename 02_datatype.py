# 資料: 程式的基本單位
123   #數字
"Hi"  #字串
True  #布林值
# List(可變列表): 有順序、「可」變動的資料集合
[3, 4, 5]
["hello", "hi"]
# Tuple(固定列表): 有順序、「不可」變動的資料集合
(3, 4, 5)
("hello", "hi")
# Set(集合): 沒有順序的概念
{3, 4, 5}
{"hello", "hi"}
# 字典: 鍵值對(Key-Value Pair)的集合
{"apple": "蘋果", "banana":"香蕉"}

# 變數: 可用來存放資料的自訂名稱
# 變數名稱 = 資料
x=3
# print(資料)
print(x) 
x=True #變數的彈性(取代舊的資料)
print(x)